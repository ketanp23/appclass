#Updating String at a existing chreacter number
var1 = 'Hello World!'

#Updating String at existing charecter number 5 i.e. Space
print("Updated String :- ", var1[0:6] + 'Python')

#Update at last charecter 11th i.e.!
print("Updated String :- ", var1[0:12] + 'Python')

#Concatanation will happen using + symbol
print(var1 + 'Python')

#Concat with a space
print(var1 + " " + 'Python')  # space between double quotes is necessary

